# Function printing the parameter
def printName(name):
    print(name)

printName("Omer")


# Function without parameters
def addTwoNumbers():
    a = int(input("First number: "))
    b = int(input("Second number: "))
    x = a + b
    print(x)

addTwoNumbers()


# Function with parameters
def addThreeNumbers(a, b, c):
    x = a + b + c
    print(x)

addThreeNumbers(2, 3, 4)


# Function returning a value
def returningAValue(value):
    return value

print(returningAValue(15))





